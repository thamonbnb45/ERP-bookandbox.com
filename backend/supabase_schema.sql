-- แผนผังฐานข้อมูล Supabase PostgreSQL (Bookandbox ERP)
-- กรุณาก๊อปปี้โค้ดทั้งหมดนี้ไปวางในเมนู "SQL Editor" บนเว็บ Supabase แล้วกด RUN ครับ

-- 1. Employee Table
CREATE TABLE employee (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    role TEXT NOT NULL
);

-- 2. Customer Table
CREATE TABLE customer (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    contact_person TEXT,
    credit_limit REAL DEFAULT 0,
    phone TEXT,
    customer_type TEXT DEFAULT 'B2B',
    balance REAL DEFAULT 0
);

-- 3. Product Table
CREATE TABLE product (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    type TEXT NOT NULL,
    base_price REAL NOT NULL
);

-- 4. Job Order Table
CREATE TABLE job_order (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    customer_id BIGINT REFERENCES customer(id),
    product_id BIGINT REFERENCES product(id),
    quantity BIGINT,
    total_price REAL,
    status TEXT DEFAULT 'pending',
    production_stage TEXT DEFAULT 'planning',
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

-- 5. Lead Contact Table
CREATE TABLE lead_contact (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    line_user_id TEXT UNIQUE NOT NULL,
    original_name TEXT,
    avatar_url TEXT,
    erp_alias_name TEXT,
    tags JSONB DEFAULT '[]'::jsonb,
    sales_status TEXT DEFAULT 'i',
    customer_id BIGINT REFERENCES customer(id),
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

-- 6. Chat Message Table
CREATE TABLE chat_message (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    lead_id BIGINT REFERENCES lead_contact(id),
    sender TEXT NOT NULL,
    type TEXT NOT NULL,
    text_content TEXT,
    media_url TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

